                    <h3>Macam-Macam Bahasa Pemrograman</h3>
                        <br>
                        <div class="image text-center">
                            <img src="img/artikel5.jpg" class="col-md-8">
                        </div>
                        <br>
                            <p style="margin-top: 10px;">Jika dilihat dari sejarahnya, bahasa pemograman kini telah menginjak pada fase evolusi kelima. Perkembangan bahasa pemograman telah berlangsung selama puluhan tahun. Tentu saja bisa dipastikan bahwa terdapat banyak jenis bahasa pemograman yang pernah digunakan.</p>
                            <p style="margin-top: 10px;"><b>1. FORTRAN</b></p>
                            <p style="margin-top: 10px;">FORTRAN adalah singkatan dari Formula Translation. Dianggap sebagai bahasa tertua yang masih dipakai saat ini. NASA adalah salah satu penggunanya. Bahasa ini banyak digunakan untuk pengembangan superkomputer, intelejensi buatan (AI), dan program bisnis.</p>
                            <p style="margin-top: 10px;"><b>2. Visual Basic</b></p>
                            <p style="margin-top: 10px;">Turunan dari bahasa BASIC yang sederhana. Bahasa ini populer ketika fase awal perkembangan komputer personal. Visual Basic dianggap mudah dan cepat dalam penerapannya. Juga masih kompatibel untuk pemograman yang lebih kompleks.</p>
                            <p style="margin-top: 10px;"><b>3. C</b></p>
                            <p style="margin-top: 10px;">Bahasa C pertama kali muncul pada tahun 1972. Ini merupakan hasil pengembangan dari Bahasa B, dan merupakan tahap awal dari sistem bahasa pemograman. Bahasa C dapat dipakai untuk berbagai aplikasi. Apple menggunakannya sampai saat ini.</p>
                            <p style="margin-top: 10px;"><b>4. C++</b></p>
                            <p style="margin-top: 10px;">Generasi lanjutan dari Bahasa C dengan fitur-fitur lebih kompleks. Bahasa ini adalah yang pertama kali menerapkan konsep Object Oriented. C++ dapat dipakai untuk mendukung penggunaan bahasa tingkat rendah, sementara masih termasuk dalam tingkat menengah. </p>
                            <p style="margin-top: 10px;"><b>5. Java</b></p>
                            <p style="margin-top: 10px;">Adalah bahasa pemograman paling populer dan paling banyak digunakan di dunia. Fleksibilitas menjadi keunggulannya, dapat dipakai untuk berbagai kebutuhan. Mulai dari pemograman jaringan, pengembangan website, perangkat lunak, dan sebagainya.</p>
                            <p style="margin-top: 10px;"><b>6. PHP</b></p>
                            <p style="margin-top: 10px;">Singkatan dari Personal Home Page. Paling banyak dipakai untuk pengembangan website. Raksasa media sosial, Facebook merupakan salah satu perusahaan yang setia menggunakan bahasa pemograman satu ini.</p>
                            <p style="margin-top: 10px;"><b>7. CSS</b></p>
                            <p style="margin-top: 10px;">CSS adalah bahasa Cascading Style Sheet dan biasanya digunakan untuk mengatur tampilan elemen yang tertulis dalam bahasa markup, seperti HTML. CSS berfungsi untuk memisahkan konten dari tampilan visualnya di situs.</p>
