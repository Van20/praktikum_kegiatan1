                  <h3>Fungsi Bahasa Pemrograman</h3>
                  <br>
                  <br>
                  <div class="image text-center">
                  <img src="img/artikel2.jpg" class="col-md-8">
                  </div>
                  <br>
                  <br>  
                  <p style="margin-top: 10px;">Fungsi dasar dari bahasa pemograman adalah agar komputer dapat mengolah data sesuai dengan alur yang dibuat secara sistematis oleh penyusunnya. Lebih lanjut, pemograman dapat memudahkan pengoperasian sebuah mesin sehingga meringankan pekerjaan manusia.</p>
                  <p style="margin-top: 10px;">Produksi bahasa pemograman dilakukan melalui proses yang cukup kompleks. Dimulai dengan penyusunan, pengujian, analisis, penyuntingan, hingga optimalisasi. Kode sumber disusun, kemudian diubah menjadi kode mesin, lalu diterjemahkan oleh prosesor sebagai perintah.</p>
                  <p style="margin-top: 10px;">Bahasa pemograman bisa digunakan untuk membuat program atau perangkat lunak, sekaligus menjadi perintah bagi perangkat keras komputer untuk memulainya agar kemudian dapat dioperasikan oleh pengguna.</p>
                  <p style="margin-top: 10px;">Contoh-contoh konkret dari penerapan bahasa pemograman ini, antara lain biasa ditemukan pada perangkat komputer kasir swalayan, lampu lalu lintas, signage lampu LED bergerak, aplikasi smartphone dan komputer, website, dan lain sebagainya.</p>
                  <p style="margin-top: 10px;">Terdapat berbagai macam bahasa pemograman yang dikenal saat ini. Di antaranya Visual Basic, Java, C++, dan yang terbaru, Bosque yang dikembangkan oleh Microsoft. Tentang jenis-jenis bahasa pemograman tersebut akan dibahas lebih lanjut di poin tersendiri.</p>

