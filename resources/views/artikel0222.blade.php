                    <div class="container">
                      <h3>Tingkatan Bahasa Pemrograman</h3>
                      <br>
                      <div class="image text-center">
                        <img src="img/artikel3.jpg" class="col-md-8">
                      </div>
                      <br>
                      <p style="margin-top: 10px;">Berbagai jenis bahasa pemograman tersebut hanya dapat diterapkan menurut fungsi dan disesuaikan dengan perangkat penggunaannya. Hal ini kemudian menjadi dasar klasifikasi bahasa pemograman yang secara umum dibagi menjadi empat kategori, yaitu:</p>

                        <table class="">
                            <tr>
                                <ul>
                                    <li>Object Oriented Language</li>
                                    <li>High Level Language</li>
                                    <li>Middle Level Language</li>
                                    <li>Low Level Language</li>
                                </ul>
                            </tr>
                        </table>

                        <p style="margin-top: 10px;">Selain itu, bahasa pemograman juga diklasifikasikan berdasarkan kedekatannya dengan perangkat komputer. Terdapat empat jenis bahasa pemograman dalam kategori ini, di antaranya:</p>
                        <table class="">
                            <tr>
                                <ul>
                                    <li><b>Bahasa Mesin.</b> Berupa bahasa biner dengan kode angka 0 dan 1.</li>
                                    <li><b>Bahasa Tingkat Rendah.</b> Istilah lain dari bahasa rakitan dengan kode huruf singkat.</li>
                                    <li><b>Bahasa Tingkat Menengah.</b> Menggabungkan kode kata-kata dan simbol.</li>
                                    <li><b>Bahasa Tingkat Tinggi.</b> Menggunakan kode dari istilah yang biasa dipakai manusia.</li>
                                </ul>
                            </tr>
                        </table>
                        <p style="margin-top: 10px;">Menurut kategori-kategori di atas, kita bisa menemukan bahwa bahasa pemograman memiliki tiga tingkatan. Penggolongan tersebut diterapkan berdasarkan tingkat kesulitan dan aplikasinya pada perangkat komputer. Berikut tingkatan bahasa pemograman yang umum dikenal.</p>
                        <p style="margin-top: 10px;"><b>1. Bahasa Tingkat Rendah</b></p>
                        <p style="margin-top: 10px;">Tingkatan ini termasuk generasi paling awal dari bahasa pemograman. Umumnya masih menggunakan kode-kode biner atau bahasa mesin berupa huruf dan angka. Instruksi yang diterapkan pada mesin atau perangkat masih terbatas dan sederhana. </p>
                        <p style="margin-top: 10px;"><b>2. Bahasa Tingkat Menengah</b></p>
                        <p style="margin-top: 10px;">Setingkat di atas kode mesin, bahasa pemograman tingkat menengah mulai menggunakan kode huruf, angka, dan simbol khusus. Beberapa contoh bahasa tingkat menengah, antara lain Assembler dan MASM (Microsoft Macro Assembler). </p>
                        <p style="margin-top: 10px;"><b>3. Bahasa Tingkat Tinggi/High Level Language</b></p>
                        <p style="margin-top: 10px;">Bahasa inilah yang paling banyak dipakai saat ini. Biasa digunakan pada pemograman berbasis website dan basis data. Baik untuk diaplikasikan pada perangkat komputer desktop maupun mobile. Contohnya bahasa Turbo C++, Visual Basic, Delphi, PHP, dan Turbo Pascal.</p>
                        <p style="margin-top: 10px;">Object Oriented Language  dikatakan termasuk dalam kategori bahasa tinggkat tinggi. Pasalnya, jenis ini juga kerap kali menggunakan basis data atau jaringan. Namun, ada pula yang menggolongkannya di tingkatan Very High Level, karena banyak melibatkan instruksi otomatis.</p>
                        <p style="margin-top: 10px;">Tingkatan bahasa Very High Level  Language (VHLL) termasuk penggolongan baru yang masih diperdebatkan. Umumnya, digunakan untuk memprogram website dinamis dan interaktif, menggabungkan konsep Object Oriented dan Visual.</p>
                        <p style="margin-top: 10px;">Beberapa jenis bahasa pemograman yang awalnya dikategorikan sebagai VHLL, antara lain Perl, Phyton, Ruby, dan Visual Basic. Namun, kemunculan PHP, Java, dan Visual Basic .NET, menggeser kedudukan Perl dan jenis bahasa lain yang segenerasi dengannya.</p>

