                <p style="margin-top: 10px;">Pada umumnya, bahasa merupakan alat komunikasi manusia. Menurut KBBI, bahasa diartikan sebagai lambang bunyi yang arbitrer atau bebas, yang digunakan oleh anggota suatu masyarakat untuk bekerja sama, berinteraksi, dan mengidentifikasi diri.</p>
                <p style="margin-top: 10px;">Manusia bisa menyampaikan informasi melalui percakapan bahasa, baik lisan maupun tulisan. Namun informasi hanya bisa tersampaikan dengan baik apabila lawan bicara memahami bahasa yang digunakan. Dan sistem tersebut berlaku pula pada bahasa pemograman.</p>
                <p style="margin-top: 10px;">Jika komunikasi antar manusia menggunakan bahasa berbentuk kata-kata atau istilah, untuk dapat berkomunikasi dengan perangkat komputer, dibutuhkan bentuk bahasa berbeda. Bahasa ini dinyatakan dalam kode-kode unik yang disebut bahasa pemograman.</p>
                <p style="margin-top: 10px;">Untuk mengetahui pengertian bahasa pemograman, pahamilah terlebih dahulu tentang pemograman itu sendiri. Pemograman merupakan aktivitas pembuatan program. Sedang program adalah instruksi tertulis yang membuat komputer dapat beroperasi.</p>
                <p style="margin-top: 10px;">Tanpa adanya program, mustahil komputer bisa digunakan. Pembuat program memberi perintah pada komputer dalam bentuk bahasa pemograman. Kemudian peranti tertentu pada komputer menerjemahkannya sehingga program tersebut bisa berjalan.</p>
                <p style="margin-top: 10px;">Dari sini, bisa disimpulkan bahwa bahasa pemograman adalah sebuah sistem komunikasi yang digunakan untuk memberi perintah pada perangkat komputer agar menjalankan fungsi tertentu yang spesifik.</p>
                <p style="margin-top: 10px;">Bahasa pemograman terdiri dari serangkaian aturan sintaks dan semantik yang digunakan untuk mendefinisikan program komputer. Sistemnya terdiri dari beberapa tingkatan untuk memberi perintah pada setiap peranti, utilitas, dan aplikasi yang dipakai dalam pengoperasian komputer.</p>
                <p style="margin-top: 10px;">Setiap jenis bahasa memiliki struktur penyusunnya, berupa prosedur sistematis yang dinyatakan dalam kode untuk membuat perintah. Ini disebut dengan algoritma, dan setiap jenis bahasa pemograman menerapkan kategori struktur berikut ini.</p>
                <p style="margin-top: 10px;"><b>1. Runtutan</b></p>
                <p style="margin-top: 10px;">Struktur runtutan dimulai dari langkah awal, yaitu instruksi pertama, kedua, ketiga, dan seterusnya secara berurutan. Setiap instruksi hanya bisa dimulai setelah instruksi sebelumnya selesai dijalankan. Struktur ini selalu dipakai dalam jenis bahasa pemograman apa saja.</p>
                <p style="margin-top: 10px;"><b>2. Perulangan</b></p>
                <p style="margin-top: 10px;">Struktur perulangan membuat program dapat menjalankan perintah secara berkelanjutan hingga berhenti pada kondisi tertentu. Penghentian perulangan pun dapat diatur sedemikian rupa dengan kode-kode perintah khusus.</p>
                <p style="margin-top: 10px;"><b>3. Percabangan</b></p>
                <p style="margin-top: 10px;">Di sini, bahasa pemograman mulai beranjak ke tingkat lanjut. Struktur bahasa pemograman dapat berupa percabangan yang berarti menginstruksikan pada komputer agar dapat membuat keputusan sendiri berdasarkan pilihan syarat-syarat tertentu.</p>
                <p style="margin-top: 10px;">Pengoperasian program melibatkan beberapa peranti keras komputer. Program terlebih dulu disimpan dalam memori (RAM) sebelum sistem operasi dapat menjalankannya. Sedang prosesor  berperan untuk mengeksekusi perintah demi perintah pada saat program tersebut dioperasikan.</p>


