                    <h3>Sejarah Bahasa Pemrograman</h3>
                        <br>
                        <div class="image text-center">
                            <img src="img/artikel4.jpg" class="col-md-8">
                        </div>
                        <br>
                            <p style="margin-top: 10px;">Tahukah Anda jika ternyata dasar-dasar bahasa pemograman pertama kali diletakkan pada pertengahan abad ke-19? Peletak dasar-dasar tersebut adalah Ada Lovelace, wanita kelahiran London tahun 1815.</p>
                            <p style="margin-top: 10px;">Kiprah Ada Lovelace dalam dunia pemograman dimulai ketika ia bertemu dengan Charles Babbage pada usia 17 tahun. Charles Babbage sendiri adalah penemu perangkat komputer mekanis pertama, dikenal pula sebagai bapak komputer dunia.</p>
                            <p style="margin-top: 10px;">Kolaborasi Lovelace dan Babbage kemudian membuahkan Mesin Analitik (Analytical Engine), yaitu sebuah alat hitung otomatis yang dioperasikan menggunakan instruksi dari kode-kode huruf, simbol, dan angka.</p>
                            <p style="margin-top: 10px;">Ada Lovelace juga berteori tentang metode menerapkan instruksi berulang-ulang pada sebuah mesin. Dan teori inilah yang kemudian menjadi dasar dari penggunaan struktur perulangan pada bahasa pemograman komputer masa kini.</p>
                            <p style="margin-top: 10px;">Komputer bertenaga listrik mulai dikembangkan sekitar tahun 1930-an. Kala itu, para praktisi mengalami kesulitan dengan pengembangan dan perbaikan program. Lalu, muncullah bahasa tingkat menengah atau bahasa rakitan yang salah satunya dicetuskan oleh Konrad Zuse (1943). </p>
                            <p style="margin-top: 10px;">Namun, bahasa tingkat menengah masih dipandang menyulitkan dan kurang praktis. Hingga kemudian, para praktisi menyusun bahasa jenis baru yang lebih baik sejak tahun 1950-an. FORTRAN, LISP, dan COBOL, adalah beberapa jenis bahasa paling inovatif pada waktu itu.</p>
                            <p style="margin-top: 10px;">Bahasa-bahasa tersebut masih digunakan hingga kini. Bahkan menjadi yang tertua di antara bahasa yang masih dipakai. Bahasa pemograman sendiri mengalami evolusi secara terus menerus, terutama sejak kemunculan Pascal pada tahun 1970 dan C dua tahun kemudian.</p>
                            <p style="margin-top: 10px;">Pascal dan C termasuk bahasa pemograman generasi ketiga. Kemudian berkembang lagi pada periode 1980-an dengan lahirnya bahasa pemograman generasi keempat. C++ yang merupakan turunan dari Bahasa C dan PERL adalah dua di antaranya.</p>
                            <p style="margin-top: 10px;">Kemudian, bahasa pemograman mengalami evolusi terakhirnya pada dekade 1990-an. Pada waktu itu, generasi kelima mulai bermunculan. Berawal dari Phyton dan disusul oleh Ruby, Java, PHP, serta Javascript yang dipakai Google sekarang.</p>
                            <p style="margin-top: 10px;">Para praktisi programming belum berhenti melakukan inovasi baru dalam bidang ini. Lahirnya konsep bahasa HTML/CSS, serta generasi terbaru rilisan Microsoft, Bosque, menandakan bahwa revolusi bahasa pemograman masih akan terus berlanjut.</p>
